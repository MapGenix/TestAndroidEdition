namespace Mapgenix.GSuite.Android
{
    /// <summary>
    /// Enumeration for the control point type when editing a shape.
    /// The default value is None
    /// </summary>
    public enum ControlPointType
    {
        None = 0,
        Drag = 1,
        Rotate = 2,
        Resize = 3,
        Vertex = 4
    }
}